;
; bootsector.asm
; Prints a single character to the screen
;
    ;------------------------------------------------------------------
    ; Bootsector is loaded at 0x7C00
    ;------------------------------------------------------------------
    org 0x7C00

    ;------------------------------------------------------------------
    ; Print a Subscribe Message :)
    ;------------------------------------------------------------------
    mov si, subscribe_message
    call print_string

    ;------------------------------------------------------------------
    ; Bootloader End
    ;------------------------------------------------------------------

    cli                             ; disable interrupts
    hlt                             ; halt the system

    ;------------------------------------------------------------------
    ; Helpful includes
    ;------------------------------------------------------------------
    %include "print_string.asm" 

    ;------------------------------------------------------------------
    ; Data
    ;------------------------------------------------------------------
subscribe_message:
    db 'Please subscribe! Thank you, kindly :)', 13, 10, 0

    ;-----------------------------------------------------------------
    ; Magic Number and padding
    ;-----------------------------------------------------------------
    times 510 - ($-$$) db 0         ; pad out the file with 0's to 510
    dw 0xAA55                       ; the magic number!!!
